function outlookWeekCalc(date) {
	var strDate = date.getFullYear() + "-" + (date.getMonth()+1) + "-" + date.getDate();
	//console.log(strDate);
	if (outlookWeekNumMap.hasOwnProperty(strDate)) {
		return outlookWeekNumMap[strDate];
	}
	return 99;
}

